package com.itheima.math;

public class Demo {
    public static void main(String[] args) {
        // abs 打印两个数的差值
        System.out.println(Math.abs(10 - 5));
        // ceil：向上取整
        System.out.println(Math.ceil(10.1));
        // floor：向下取整
        System.out.println(Math.floor(10.1));
        // round：四舍五入
        System.out.println(Math.round(10.1));
        // max：返回参数中的最大值
        System.out.println(Math.max(14, 15));
        // min：返回返回参数中的最小值
        System.out.println(Math.max(14, 15));
        // 三元运算
        System.out.println(20 > 15 ? 20 : 15);


    }
}
